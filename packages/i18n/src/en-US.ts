type StringifyValues<T> = T extends string
  ? string
  : T extends object
    ? { [K in keyof T]: StringifyValues<T[K]> }
    : T

export default enUS

export type Messages = StringifyValues<typeof enUS>

/** Dot-notation keys, e.g. `nav.create` */
export type MessageKey = {
  [K in keyof Messages]: Messages[K] extends string
    ? K
    : {
        [K2 in keyof Messages[K]]: Messages[K][K2] extends string
          ? `${K & string}.${K2 & string}`
          : {
              [K3 in keyof Messages[K][K2]]: Messages[K][K2][K3] extends string
                ? `${K & string}.${K2 & string}.${K3 & string}`
                : `${K & string}.${K2 & string}.${K3 & string}.${string}`
            }[keyof Messages[K][K2]]
      }[keyof Messages[K]]
}[keyof Messages]
